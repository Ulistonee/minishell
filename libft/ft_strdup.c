#include "libft.h"

char		*ft_strdup(const char *str)
{
	char	*copy;
	int		i;

	i = 0;
	if (!(copy = (char*)malloc(sizeof(char) * ft_strlen(str) + 1)))
		return (NULL);
	while (str[i] != '\0')
	{
		copy[i] = str[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
